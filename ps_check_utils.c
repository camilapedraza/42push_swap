#include "push_swap.h"

int is_digit(int c)
{
	if (!(c >= 48 && c <= 57))
		return (0);
	return (1);
}

int	is_space(int c)
{
	if (!(c == ' ') && !(c >=9 && c <= 13))
		return (0);
	return (1);
}